#!/usr/bin/env Rscript
# Generate the small R reference fixture consumed by validate_against_r.py.
# Requires: R packages gamlss, gamlss.dist, jsonlite.

suppressPackageStartupMessages({
  library(gamlss)
  library(gamlss.dist)
  library(jsonlite)
})

args <- commandArgs(trailingOnly = TRUE)
out_path <- if (length(args) >= 1) args[[1]] else "benchmarks/r_reference_results.json"

make_normal_linear <- function() {
  seed <- 42L
  n <- 200L
  x <- seq(0, 5, length.out = n)
  y <- 2 + 3 * x + sin(x)
  df <- data.frame(y = y, x = x)

  fit <- gamlss(
    y ~ x,
    sigma.formula = ~ 1,
    family = NO(),
    data = df,
    trace = FALSE
  )

  mu_coef <- coef(fit, what = "mu")
  sigma_coef <- coef(fit, what = "sigma")

  list(
    id = "normal_linear",
    family = "NO",
    formula = "y ~ x",
    seed = seed,
    n = n,
    r_deviance = unname(as.numeric(fit$G.deviance)),
    r_mu_coef_intercept = unname(as.numeric(mu_coef[["(Intercept)"]])),
    r_mu_coef_x = unname(as.numeric(mu_coef[["x"]])),
    r_sigma_coef_intercept = unname(as.numeric(sigma_coef[["(Intercept)"]]))
  )
}

fixture <- list(
  generated_at = format(Sys.time(), "%Y-%m-%dT%H:%M:%SZ", tz = "UTC"),
  status = "generated_by_r",
  note = "Generated by benchmarks/generate_r_reference_results.R in an environment with R gamlss/jsonlite installed.",
  r_version = paste(R.version$major, R.version$minor, sep = "."),
  gamlss_version = as.character(packageVersion("gamlss")),
  test_cases = list(make_normal_linear())
)

write_json(fixture, path = out_path, auto_unbox = TRUE, pretty = TRUE, null = "null")
cat(sprintf("Wrote R reference fixture to %s\n", out_path))
